    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try
        {
            int r = jTable1.getSelectedRow();
            String Admn = (jTable1.getModel().getValueAt(r, 0).toString());
            connect();
            String q = "Select students.Name, fees.Class, fees.Batch, Total_Fees, Due_Fees, Due_Date from fees, students where fees.AdmNo=Students.AdmNo && fees.AdmNo = '"+Admn+"';";
            rs = s.executeQuery(q);
            if (rs.next())
            {
                String Nm = rs.getString("Name");
                int Cls = rs.getInt("Class");
                String Batch = rs.getString("Batch");
                int Total_Fees = rs.getInt("Total_Fees");
                int Due_Fees = rs.getInt("due_fees");
                String Due_Date = rs.getString("due_date");
                Admno_lb.setText(Admn);
                name_lb.setText(Nm);
                cls_lb.setText(Integer.toString(Cls));
                batch_lb.setText(Batch);
                total_fees_lb.setText(Integer.toString(Total_Fees));
                d_date_lb.setText(Due_Date);
                jTextField10.setText(Integer.toString(Due_Fees));
                int con = JOptionPane.showConfirmDialog(null, "Do you want to clear all dues?", "Clear Dues", YES_NO_OPTION);
                if (con == 0)
                {
                    String q1 = "Update fees set Due_Fees = 0 where AdmNo = '"+Admn+"';";
                    s.executeUpdate(q1);
                    String q2 = "Update fees set due_date = NULL where AdmNo = '"+Admn+"';";
                    s.executeUpdate(q2);
                    JOptionPane.showMessageDialog(null, "Dues cleared", "Dues Cleared", INFORMATION_MESSAGE);
                }
            }
            String q1 = "Select AdmNo, Class, Batch, Due_Fees, Due_Date from fees;";
            rs = s.executeQuery(q1);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

