    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        try {
            connect();
            int r = jTable4.getRowCount();
            int i = 0;
            int a1=3 ,b1=3;
            while (i<r)
            {
                String m = jTable4.getValueAt(i,0).toString();
                String a = jTable4.getValueAt(i,1).toString();
                if (a.equalsIgnoreCase("true"))
                {
                    a1 = 1;
                    String q1 = "Update Material set A = curdate() where class = '"+cls1+"' && Material = '"+m+"';";
                    s.executeUpdate(q1);
                }
                else if (a.equalsIgnoreCase("false"))
                {
                    a1 = 0;
                    String q2 = "Update Material set A = NULL where class = '"+cls1+"' && Material = '"+m+"';";
                    s.executeUpdate(q2);
                }
                String b = jTable4.getValueAt(i,2).toString();
                if (b.equalsIgnoreCase("true"))
                {
                    b1 = 1;
                    String q3 = "Update Material set B = curdate() where class = '"+cls1+"' && Material = '"+m+"';";
                    s.executeUpdate(q3);
                }
                else if (b.equalsIgnoreCase("false"))
                {
                    b1 = 0;
                    String q4 = "Update Material set B = NULL where class = '"+cls1+"' && Material = '"+m+"';";
                    s.executeUpdate(q4);
                }
                String q5 = "Update Material1 set A = '"+a1+"' where class = '"+cls1+"' && Material = '"+m+"';";
                s.executeUpdate(q5);
                String q6 = "Update Material1 set B = '"+b1+"' where class = '"+cls1+"' && Material = '"+m+"';";
                s.executeUpdate(q6);
                i = i+1;
            }
            JOptionPane.showMessageDialog(rootPane, "Record Updated", "Record Updated", INFORMATION_MESSAGE);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton13ActionPerformed

