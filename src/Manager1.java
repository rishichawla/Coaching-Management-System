    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        try
        {
            int r = jTable3.getSelectedRow();
            String EmpCode = (jTable3.getModel().getValueAt(r, 0).toString());
            connect();
            String q = "Select EmpCode, Name, Department, Phone, Salary from Employees where EmpCode = '"+EmpCode+"';";
            rs = s.executeQuery(q);
            if (rs.next())
            {
                String Nm = rs.getString("Name");
                String Department = rs.getString("Department");
                String Phone = rs.getString("Phone");   
                int Salary = rs.getInt("Salary");
                EmpCode_lb.setText(EmpCode);
                nm_lb.setText(Nm);
                department_lb.setText(Department);
                Phone_tf.setText(Phone);
                jSpinner1.setValue(Salary);
                int con = JOptionPane.showConfirmDialog(null, "Do you want to delete this record?", "Delete Record", YES_NO_OPTION);
                if (con == 0)
                {
                    String q1 = "Delete from employees where EmpCode = '"+EmpCode+"';";
                    s.executeUpdate(q1);
                    String q2 = "Delete from id_employees where Username = '"+EmpCode+"';";
                    s.executeUpdate(q2);
                    JOptionPane.showMessageDialog(null, "Record Deleted", "Record Deleted", INFORMATION_MESSAGE);
                }
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_removeActionPerformed

