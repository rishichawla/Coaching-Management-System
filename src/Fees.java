    private void show_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_buttonActionPerformed
String Admn = Admn_tf.getText();
if (Admn.isEmpty())
{
    JOptionPane.showMessageDialog(null, "Enter Admission Number", "Field Empty", ERROR_MESSAGE);
}
else {
int DAmt=0; String d_date="";
try
{
    connect();
    String q = "Select due_fees, due_date from fees where AdmNo = '"+Admn+"';";
    rs = s.executeQuery(q);
    if (rs.next())
    {
        DAmt = rs.getInt("due_fees");
        d_date = rs.getString("due_date");
    }
    else
    {
        JOptionPane.showMessageDialog(null, "No such Record");
    }
    DAmt_lb.setText(Integer.toString(DAmt));
    due_date_lb.setText(d_date);
    pay_button.setEnabled(true);
    Amount_slider.setMaximum(DAmt);
    Amount_slider.setSnapToTicks(true);
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(null, e.getMessage());
}}
    }//GEN-LAST:event_show_buttonActionPerformed

    private void pay_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_buttonActionPerformed
String Admn = Admn_tf.getText();
int Amt = Amount_slider.getValue();
int DAmt=0; String due_date;
try
{
    connect();
    String q = "Select due_fees from fees where AdmNo = '"+Admn+"';";
    rs = s.executeQuery(q);
    if (rs.next())
    {
        DAmt = rs.getInt("due_fees");
    }
    if (DAmt==Amt)
    {
        DAmt = 0;
        due_date_lb.setText("");
        String q1 = "Update fees set due_date = NULL where AdmNo = '"+Admn+"';";
        s.executeUpdate(q1);
    }
    else
    {
        DAmt = DAmt - Amt;
    }
    DAmt_lb.setText(Integer.toString(DAmt));
    Amount_slider.setValue(0);
    String q2 = "Update fees set due_fees = '"+DAmt+"' where AdmNo = '"+Admn+"';";
    s.executeUpdate(q2);
    JOptionPane.showMessageDialog(rootPane, "Fees Paid", "Fees Paid", INFORMATION_MESSAGE);
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(null, e.getMessage());
}
    }//GEN-LAST:event_pay_buttonActionPerformed

