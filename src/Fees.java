    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
try
{
    int r = jTable1.getSelectedRow();
    String Admn = (jTable1.getModel().getValueAt(r, 0).toString());
    connect();
    String q = "Select students.Admno, students.Name, fees.Class, fees.Batch, Total_Fees, Due_Fees, Due_Date from fees, students where fees.AdmNo=Students.AdmNo && fees.AdmNo = '"+Admn+"';";
    rs = s.executeQuery(q);
    if (rs.next())
    {
        String Nm = rs.getString("Name");
        int Cls = rs.getInt("Class");
        String Batch = rs.getString("Batch");
        int Total_Fees = rs.getInt("Total_Fees");
        int Due_Fees = rs.getInt("due_fees");
        String Due_Date = rs.getString("due_date");
        Admno_lb_f.setText(Admn);
        name_lb_f.setText(Nm);
        cls_lb_f.setText(Integer.toString(Cls));
        batch_lb_f.setText(Batch);
        total_fees_lb_f.setText(Integer.toString(Total_Fees));
        d_date_lb_f.setText(Due_Date);
        jTextField10.setText(Integer.toString(Due_Fees));
    }
}
catch (Exception e)
{
    JOptionPane.showMessageDialog(null, e.getMessage());
}        
    }//GEN-LAST:event_jTable1MouseClicked

