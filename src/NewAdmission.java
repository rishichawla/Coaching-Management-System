    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
nam = jTextField1.getText();
cls = Integer.parseInt(jComboBox2.getSelectedItem().toString());
Batch = jComboBox3.getSelectedItem().toString();
DOB = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
if (nam.isEmpty())
{
    JOptionPane.showMessageDialog(null, "Please Enter Name", "Name not entered", ERROR_MESSAGE);
}
else if (path_tf.getText().isEmpty())
{
    JOptionPane.showMessageDialog(null, "Please Attach Profile Picture", "Profile Picture not attached", ERROR_MESSAGE);
}
else 
{
    int s1=0;
    try
    {
        connect();
        String q = "Select count(AdmNo) from Students where class = '"+cls+"';";
        rs = s.executeQuery(q);
        if (rs.next())
        {
            s1 = rs.getInt("count(AdmNo)");
        }
        if (s1<9)
        {
            Admn = "CC"+Integer.toString(cls)+"0"+Integer.toString(s1+1);
        }
        else
        {
            Admn = "CC"+Integer.toString(cls)+Integer.toString(s1+1);
        }
        jTabbedPane1.setSelectedIndex(1);
        jPanel2.setEnabled(true);
        jTabbedPane1.setEnabledAt(1, true);
        jProgressBar1.setValue(25);
    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    }//GEN-LAST:event_jButton1ActionPerformed

