public String Title(String n)
    {
        int i = 0;
        String nmt = n;
        do
        {
            if (n.charAt(i)== ' ')
            {
                if (n.length()>i+1)
                {
                    nmt = n.substring(0,1).toUpperCase()+n.substring(1,i+1).toLowerCase()+n.substring(i+1,i+2).toUpperCase()+n.substring(i+2).toLowerCase();
                }
                else
                {
                    nmt = n.substring(0,1).toUpperCase()+n.substring(1).toLowerCase();
                }
            }
            i=i+1;
        } while(i<n.length());
        return nmt;
    }
